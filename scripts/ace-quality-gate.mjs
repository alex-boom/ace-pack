import { access, chmod, mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { pathToFileURL } from 'node:url'

import { validateAgentMemory } from './agent-memory-lib.mjs'
import { classifyRepositoryTask } from './ai-task-classify.mjs'
import { isSmallLowRiskClassification, validateFinishRequirements } from './ai-task-finish.mjs'
import {
  extractMarkdownSection,
  getArgValue,
  hasMeaningfulContent,
  parseCliArgs,
  readMemoryFile,
  readTextIfExists,
} from './ai-memory-utils.mjs'

const ACE_HOOK_MARKER = '# ACE QUALITY GATE HOOK'
const GITHUB_ACTION_PATH = '.github/workflows/ace-quality-gate.yml'

export async function runQualityGate(rootDir, options = {}) {
  const humanOverride = normalizeHumanOverride(options.humanOverride)
  const [memoryIssues, classification, currentTaskContent, handoffContent, reflectionLogContent] =
    await Promise.all([
      validateAgentMemory(rootDir),
      classifyRepositoryTask(rootDir, {
        baseRef: options.baseRef,
        headRef: options.headRef,
      }),
      readAiFile(rootDir, 'current-task.md'),
      readAiFile(rootDir, 'session-handoff.md'),
      readAiFile(rootDir, 'reflection-log.md'),
    ])
  const issues = []

  for (const memoryIssue of memoryIssues) {
    issues.push(createMemoryIssue(memoryIssue))
  }

  if (classification.gitError) {
    issues.push({
      code: 'git-diff-unavailable',
      fix: 'Fetch the PR base ref, verify --base/--head values, or run ace:gate without PR refs for a working-tree check.',
      message: `Unable to inspect git diff for --base ${classification.baseRef} --head ${classification.headRef}.`,
    })
  }

  if (currentTaskContent !== null && handoffContent !== null && reflectionLogContent !== null) {
    for (const missingRequirement of validateFinishRequirements({
      classification,
      currentTaskContent,
      handoffContent,
      reflectionLogContent,
    })) {
      if (!shouldEnforceFinishRequirement(missingRequirement, classification)) {
        continue
      }

      issues.push(createFinishIssue(missingRequirement, classification))
    }
  }

  if (handoffContent !== null) {
    const verification = extractMarkdownSection(handoffContent, 'Verification')

    if (!isSmallLowRiskClassification(classification) && !hasMeaningfulContent(verification)) {
      issues.push({
        code: 'handoff-verification-missing',
        fix: 'Record the checks that passed in .ai/session-handoff.md Verification, or explicitly document why a check could not be run.',
        message: '.ai/session-handoff.md Verification is missing or still placeholder text.',
      })
    }
  }

  return {
    classification: summarizeClassification(classification),
    humanOverride,
    issues,
    passed: humanOverride !== null || issues.length === 0,
  }
}

export async function writeGithubActionWorkflow(rootDir) {
  const workflowPath = path.join(rootDir, GITHUB_ACTION_PATH)
  const content = `# Generated by ACE. Edit if your project needs a stricter local gate.
name: ACE Quality Gate

on:
  pull_request:

jobs:
  ace-quality-gate:
    runs-on: ubuntu-latest
    permissions:
      contents: read
    steps:
      - uses: actions/checkout@v4
        with:
          fetch-depth: 0
      - uses: actions/setup-node@v4
        with:
          node-version: 20
      - run: npm run ace:gate -- --base origin/\${{ github.base_ref }} --head HEAD
`

  await mkdir(path.dirname(workflowPath), { recursive: true })
  await writeFile(workflowPath, content, 'utf8')

  return {
    action: 'written',
    path: GITHUB_ACTION_PATH,
  }
}

export async function installPrePushHook(rootDir) {
  const gitDir = path.join(rootDir, '.git')
  const hooksDir = path.join(gitDir, 'hooks')
  const hookPath = path.join(hooksDir, 'pre-push')
  const samplePath = path.join(hooksDir, 'pre-push.ace.sample')
  const hookContent = `#!/bin/sh
${ACE_HOOK_MARKER}
npm run ace:gate
`

  try {
    await access(gitDir)
  } catch {
    throw new Error('Cannot install ACE pre-push hook because .git was not found.')
  }

  await mkdir(hooksDir, { recursive: true })

  const existingContent = await readTextIfExists(hookPath)

  if (existingContent === hookContent) {
    await chmod(hookPath, 0o755)
    return {
      action: 'unchanged',
      path: '.git/hooks/pre-push',
    }
  }

  if (existingContent !== null && !existingContent.includes(ACE_HOOK_MARKER)) {
    await writeFile(samplePath, hookContent, 'utf8')
    await chmod(samplePath, 0o755)

    return {
      action: 'sample-written',
      path: '.git/hooks/pre-push.ace.sample',
      reason: 'Existing non-ACE pre-push hook was left unchanged.',
    }
  }

  await writeFile(hookPath, hookContent, 'utf8')
  await chmod(hookPath, 0o755)

  return {
    action: existingContent === null ? 'created' : 'updated',
    path: '.git/hooks/pre-push',
  }
}

function createMemoryIssue(memoryIssue) {
  return {
    code: 'memory-invalid',
    fix: 'Run ace:init if files are missing, or restore the required ACE sections before merging.',
    message: memoryIssue,
  }
}

function createFinishIssue(missingRequirement, classification) {
  if (missingRequirement.includes('Business Value / Product Alignment')) {
    return {
      code: 'business-value-missing',
      fix: 'Fill .ai/current-task.md Business Value / Product Alignment with the user or business reason for the change.',
      message: '.ai/current-task.md Business Value / Product Alignment is incomplete.',
    }
  }

  if (missingRequirement.includes('Technical Approach')) {
    return {
      code: 'technical-approach-missing',
      fix: 'Add Option 1, Option 2, and Chosen Approach to .ai/current-task.md, then rerun ace:gate.',
      message: `${describeRisk(classification)}, but .ai/current-task.md Technical Approach is incomplete.`,
    }
  }

  if (missingRequirement.includes('Quality Review')) {
    return {
      code: 'quality-review-missing',
      fix: 'Fill .ai/session-handoff.md Quality Review with Product Alignment, Architecture, Security, and Code Quality notes.',
      message: `${classification.tier} task detected, but .ai/session-handoff.md Quality Review is incomplete.`,
    }
  }

  if (missingRequirement.includes('reflection-log')) {
    return {
      code: 'reflection-missing',
      fix: 'Add a compact .ai/reflection-log.md entry when this large task exposed useful process friction, then rerun ace:gate.',
      message: 'Large task detected, but no useful reflection entry is recorded.',
    }
  }

  return {
    code: 'finish-requirement-missing',
    fix: 'Complete the missing closeout note described by ACE, then rerun ace:gate.',
    message: missingRequirement,
  }
}

function shouldEnforceFinishRequirement(missingRequirement, classification) {
  if (!missingRequirement.includes('Quality Review')) {
    return true
  }

  return classification.tier === 'large' || classification.riskMatches.length > 0
}

function normalizeHumanOverride(value) {
  if (value === undefined || value === null || value === false) {
    return null
  }

  const reason = String(value).replace(/\s+/g, ' ').trim()

  if (!reason || reason === 'true') {
    throw new Error(
      'Human override requires a reason. Use --human-override "<reason for bypass>".',
    )
  }

  return {
    reason,
    type: 'human',
  }
}

function describeRisk(classification) {
  const matchedPaths = classification.riskMatches
    .flatMap((match) => match.matched)
    .filter(Boolean)
  const uniquePaths = [...new Set(matchedPaths)].slice(0, 3)

  if (uniquePaths.length > 0) {
    return `High-risk paths changed (${uniquePaths.join(', ')})`
  }

  if (classification.designReviewRequired) {
    return 'Design review is required for this change'
  }

  return 'ACE gate requires design notes'
}

function summarizeClassification(classification) {
  return {
    baseRef: classification.baseRef,
    changedFiles: classification.changedFiles,
    detectedTier: classification.detectedTier,
    designReviewRequired: classification.designReviewRequired,
    diffLineCount: classification.diffLineCount,
    gitError: classification.gitError,
    headRef: classification.headRef,
    reasons: classification.reasons,
    riskMatches: classification.riskMatches,
    tier: classification.tier,
  }
}

async function readAiFile(rootDir, fileName) {
  return readMemoryFile(rootDir, fileName)
}

function formatGateResult(result) {
  if (result.humanOverride) {
    const lines = [
      `[ACE GATE] Passed with human override: ${result.humanOverride.reason}`,
      `[ACE GATE] Tier: ${result.classification.tier} (detected: ${result.classification.detectedTier})`,
    ]

    if (result.issues.length > 0) {
      lines.push(`[ACE GATE] Bypassed ${result.issues.length} issue(s):`)

      for (const issue of result.issues) {
        lines.push(`  - ${issue.message}`)
      }
    }

    return lines.join('\n')
  }

  if (result.passed) {
    return [
      '[ACE GATE] Passed: ACE memory and handoff checks are ready for merge.',
      `[ACE GATE] Tier: ${result.classification.tier} (detected: ${result.classification.detectedTier})`,
    ].join('\n')
  }

  const lines = []

  for (const issue of result.issues) {
    lines.push(`[ACE GATE] Failed: ${issue.message}`)
    lines.push(`  Fix: ${issue.fix}`)
  }

  lines.push(`[ACE GATE] Gate failed with ${result.issues.length} issue(s).`)

  return lines.join('\n')
}

function formatActionResult(action) {
  if (action.reason) {
    return `[ACE GATE] ${action.path}: ${action.action}. ${action.reason}`
  }

  return `[ACE GATE] ${action.path}: ${action.action}.`
}

async function main() {
  const args = parseCliArgs(process.argv.slice(2))
  const rootDir = path.resolve(process.cwd(), getArgValue(args, 'root') ?? '.')
  const json = getArgValue(args, 'json') === 'true'
  const actions = []

  try {
    if (getArgValue(args, 'write-github-action') === 'true') {
      actions.push(await writeGithubActionWorkflow(rootDir))
    }

    if (getArgValue(args, 'install-pre-push') === 'true') {
      actions.push(await installPrePushHook(rootDir))
    }

    if (actions.length > 0) {
      if (json) {
        process.stdout.write(`${JSON.stringify({ actions, passed: true }, null, 2)}\n`)
        return
      }

      process.stdout.write(`${actions.map(formatActionResult).join('\n')}\n`)
      return
    }

    const result = await runQualityGate(rootDir, {
      baseRef: getArgValue(args, 'base'),
      headRef: getArgValue(args, 'head'),
      humanOverride: getArgValue(args, 'human-override'),
    })

    if (json) {
      process.stdout.write(`${JSON.stringify(result, null, 2)}\n`)
      process.exit(result.passed ? 0 : 1)
    }

    process.stdout.write(`${formatGateResult(result)}\n`)
    process.exit(result.passed ? 0 : 1)
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error)

    if (json) {
      process.stdout.write(
        `${JSON.stringify(
          {
            error: message,
            passed: false,
          },
          null,
          2,
        )}\n`,
      )
    } else {
      process.stderr.write(`[ACE GATE] Failed: ${message}\n`)
    }

    process.exit(1)
  }
}

if (import.meta.url === pathToFileURL(process.argv[1]).href) {
  await main()
}
